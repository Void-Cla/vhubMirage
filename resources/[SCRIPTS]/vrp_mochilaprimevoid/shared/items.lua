function ITENS.obterItemInfo(nomeItem)
    return ITENS[nomeItem]
end

function ITENS.validarItem(nomeItem)
    return ITENS[nomeItem] ~= nil
end

function ITENS.obterPesoItem(nomeItem, quantidade)
    local info = ITENS[nomeItem]
    if not info then return 0 end
    return (info.peso or 0) * (quantidade or 1)
end

function ITENS.podeDroparItem(nomeItem)
    local info = ITENS[nomeItem]
    return info and not info.bloqueado_drop or false
end

function ITENS.ehItemEspecial(nomeItem)
    local info = ITENS[nomeItem]
    return info and info.especial == true or false
end

function ITENS.podeIrParaBau(nomeItem)
    local info = ITENS[nomeItem]
    return info and info.permitido_bau == true or false
end

function ITENS.podeIrParaMarketplace(nomeItem)
    local info = ITENS[nomeItem]
    return info and info.permitido_marketplace == true or false
end

function ITENS.obterCorCategoria(categoria)
    local cores = {
        NORMAL = '#00ff88',
        LEGAL = '#0099ff',
        ILEGAL = '#ff3333',
        ESPECIAL = '#d946ef',
    }
    return cores[categoria] or '#ffffff'
end

return ITENS
